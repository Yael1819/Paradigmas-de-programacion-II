    private void mostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mostrarMouseClicked
        
    try {
        
        if (Rec_perimetro.isSelected()) {
            actualizarCampos();
            double b = Double.parseDouble(base.getText());
            double h = Double.parseDouble(altura.getText());
            double perimetro = 2 * (b + h);
            resultado.setText("Perímetro del rectángulo: " + perimetro + " cm");
        }
        
        else if (Rec_area.isSelected()) {
            actualizarCampos();
            double b = Double.parseDouble(base.getText());
            double h = Double.parseDouble(altura.getText());
            double area = b * h;
            resultado.setText("Área del rectángulo: " + area + " cm²");
        }
        
        else if (Cir_perimetro.isSelected()) {
            actualizarCampos();
            double r = Double.parseDouble(radio.getText());
            double pi = Math.PI;
            double area = pi * Math.pow(r, 2);
            resultado.setText("Área del círculo: " + area + " cm²");
        }
        
        else if (Cir_area1.isSelected()) {
            actualizarCampos();
            double r = Double.parseDouble(radio.getText());
            double pi = Math.PI;
            double perimetro = 2 * pi * r;
            resultado.setText("Perímetro del círculo: " + perimetro + " cm");
        } else {
            resultado.setText("Por favor, seleccione una opción.");
        }
    } catch (NumberFormatException e) {
        resultado.setText("ERROR VALOR INVALIDO");
    }
    }//GEN-LAST:event_mostrarMouseClicked

