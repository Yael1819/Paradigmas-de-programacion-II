private void actualizarCampos() {
    // Habilitar/deshabilitar según el botón seleccionado
    if (Rec_perimetro.isSelected() || Rec_area.isSelected()) {
        base.setEnabled(true);
        altura.setEnabled(true);
        radio.setEnabled(false);
    } else if (Cir_perimetro.isSelected() || Cir_area1.isSelected()) {
        base.setEnabled(false);
        altura.setEnabled(false);
        radio.setEnabled(true);
    } else {
        // Si no hay nada seleccionado, habilita todo o deshabilita todo según prefieras
        base.setEnabled(false);
        altura.setEnabled(false);
        radio.setEnabled(false);
    }
}
