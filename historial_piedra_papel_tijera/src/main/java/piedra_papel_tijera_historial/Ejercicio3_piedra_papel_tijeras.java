    private void mostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mostrarMouseClicked
    String[] opciones = {"piedra", "papel", "tijera"};
    Random random = new Random();
    int cpu = random.nextInt(opciones.length);
    String eleccionCPU = opciones[cpu];
    String eleccionJugador = "";

    if (piedra.isSelected()) {
        eleccionJugador = "piedra";
    } else if (papel.isSelected()) {
        eleccionJugador = "papel";
    } else if (tijera.isSelected()) {
        eleccionJugador = "tijera";
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione una opci√≥n.");
        return;
    }

    if (eleccionJugador.equals(eleccionCPU)) {
        empate++;
        resultado.setText("Jugador: " + eleccionJugador + ". CPU: " + eleccionCPU + ", Resultado: Empate");
    } else if (
        (eleccionJugador.equals("piedra") && eleccionCPU.equals("tijera")) ||
        (eleccionJugador.equals("papel") && eleccionCPU.equals("piedra")) ||
        (eleccionJugador.equals("tijera") && eleccionCPU.equals("papel"))
    ) {
        jugavic++;
        resultado.setText("Jugador: " + eleccionJugador + ". CPU: " + eleccionCPU + ", Resultado: Ganaste");
    } else {
        cpuvic++;
        resultado.setText("Jugador: " + eleccionJugador + ". CPU: " + eleccionCPU + ", Resultado: Gana la CPU");
    }
    }//GEN-LAST:event_mostrarMouseClicked

